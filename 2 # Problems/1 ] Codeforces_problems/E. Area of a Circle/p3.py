line = input()
st = ""
l = []
for i in line:
    if i == " ":
        l.append(int(st))
        st = ""
    else:
        st = st + i

l.append(int(st))

print(l[0], '+', l[1], '=', l[0] + l[1])
print(l[0], '*', l[1], '=', l[0] * l[1])
print(l[0], '-', l[1], '=', l[0] - l[1])
